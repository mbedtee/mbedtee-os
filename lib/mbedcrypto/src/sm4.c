/* T-table accelerated round function T = L(tau) */
static inline uint32_t sm4_t(uint32_t a)
{
	return sm4_t0[(a >> 24) & 0xff] ^
	       ROTL(sm4_t0[(a >> 16) & 0xff], 24) ^
	       ROTL(sm4_t0[(a >>  8) & 0xff], 16) ^
	       ROTL(sm4_t0[(a)       & 0xff], 8);
}
