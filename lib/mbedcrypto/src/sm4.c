/* Round function T = L(tau) */
static inline uint32_t sm4_t(uint32_t a)
{
	return sm4_l(sm4_tau(a));
}
#endif

/* Linear transform L' for key expansion (Section 7.3.1) */
static inline uint32_t sm4_l_prime(uint32_t b)
{
	return b ^ ROTL(b, 13) ^ ROTL(b, 23);
}

/* Key expansion transform T' = L'(tau) */
static inline uint32_t sm4_t_prime(uint32_t a)
{
	return sm4_l_prime(sm4_tau(a));
}

int mbedcrypto_sm4_setkey(struct mbedcrypto_sm4_ctx *ctx,
		const uint8_t key[MBEDCRYPTO_SM4_KEYSIZE], int dir)
{
	uint32_t k[36];
	int i = 0;

	if (!ctx || !key)
		return -EINVAL;

	memset(ctx, 0, sizeof(*ctx));
	ctx->dir = dir;

	/* MK[i] XOR FK[i] */
	for (i = 0; i < 4; i++)
		k[i] = mbedcrypto_get_be32(key + 4 * i) ^ sm4_fk[i];

	/* Generate 32 round keys */
	for (i = 0; i < 32; i++) {
		k[i + 4] = k[i] ^ sm4_t_prime(k[i + 1] ^ k[i + 2] ^ k[i + 3] ^ sm4_ck[i]);
		ctx->rk[i] = k[i + 4];
	}

	/* Reverse round keys for decryption */
	if (dir == MBEDCRYPTO_SM4_DECRYPT) {
		uint32_t tmp = 0;

		for (i = 0; i < 16; i++) {
			tmp = ctx->rk[i];
			ctx->rk[i] = ctx->rk[31 - i];
			ctx->rk[31 - i] = tmp;
		}
	}

	memset(k, 0, sizeof(k));
	return 0;
}

int mbedcrypto_sm4_ecb_crypt(const struct mbedcrypto_sm4_ctx *ctx,
		const uint8_t in[MBEDCRYPTO_SM4_BLKSIZE],
		uint8_t out[MBEDCRYPTO_SM4_BLKSIZE])
{
	uint32_t x[36];
	int i = 0;

	if (!ctx || !in || !out)
		return -EINVAL;

	/* Load input */
	for (i = 0; i < 4; i++)
		x[i] = mbedcrypto_get_be32(in + 4 * i);

	/* 32 rounds */
	for (i = 0; i < 32; i++)
		x[i + 4] = x[i] ^ sm4_t(x[i + 1] ^ x[i + 2] ^ x[i + 3] ^ ctx->rk[i]);

	/* Output (reverse order) */
	mbedcrypto_put_be32(out,      x[35]);
	mbedcrypto_put_be32(out + 4,  x[34]);
	mbedcrypto_put_be32(out + 8,  x[33]);
	mbedcrypto_put_be32(out + 12, x[32]);

	return 0;
}

void mbedcrypto_sm4_cleanup(struct mbedcrypto_sm4_ctx *ctx)
{
	if (ctx)
		memset(ctx, 0, sizeof(*ctx));
}
